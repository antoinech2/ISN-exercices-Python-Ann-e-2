#Programme qui affiche la table de multiplication de 7

#On initialise la variable qui va servir à la boucle
boucle=0

#Tant que a est intéfieur ou égal à 10:
while(boucle<=10):
    #On affiche 7 fois le nombre a
    print("7*",boucle,"=",7*boucle)
    #On incrémente la variable "a" pour passer au nombre suivant
    boucle+=1
