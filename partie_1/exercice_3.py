#Programme qui affiche les carré et les cubes des entiers de 1 à 10

#On initialise la variable qui va servir à la boucle
boucle=1

#Tant que a est intéfieur ou égal à 10:
while(boucle<=10):
    #On affiche le nombre, son carré et son cube.
    print("Nombre:",boucle,"Carré:",boucle**2,"Cube:",boucle**3)
    #On incrémente la variable "boucle" pour passer au nombre suivant
    boucle+=1
